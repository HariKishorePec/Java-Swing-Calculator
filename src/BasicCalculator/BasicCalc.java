    double PerformCalc(String op)
    {
        try
        {
            double temp=0;
            switch(op)
            {
                case "+" :
                    temp = num1 + num2;
                    break;
                case "-" :
                    temp = num1 - num2;
                    break;
                case "*" :
                    temp = num1 * num2;
                    break;
                case "/" :
                    if(num2==0)
                       // TextField.setText("Zero Division Error.");
                        num2isZero = true;
                    else 
                        temp = num1 / num2;
                    break;
                default:
                    System.err.println("MyError");
            }
            return temp;
        }catch(Exception e1){
            JOptionPane.showMessageDialog(rootPane, "Calculation Error. Code:01");
            return (1);
        }
                
    }
